package string

fun main(){
    indexcando()
}


fun indexcando(){

    val vetor = "Feliz 2022!"

    // Primeira posição
    println(vetor [0])
    println(vetor.first())

    // Última posição
    println(vetor.last())
    println(vetor[vetor.length-1])

    // Tamanho
    println(vetor.length)

    println(vetor[3])

}